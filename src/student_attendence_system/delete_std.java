    private void dltbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dltbtnActionPerformed
        // TODO add your handling code here:
        int row = srchtbl.getSelectedRow();
        String cell = srchtbl.getModel().getValueAt(row, 0).toString();
        String sql = "delete from student where s_id = "+ cell;
        try {
            pst = con.prepareStatement(sql);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Record has been deleted");
            pst = con.prepareStatement("select * from student WHERE s_id = ?");
            pst.setString(1, cell);
            rs = pst.executeQuery();
            srchtbl.setModel(DbUtils.resultSetToTableModel(rs));
           
        } catch (SQLException ex) {
            Logger.getLogger(delete_std.class.getName()).log(Level.SEVERE, null, ex);
        }  
        
        
        
    }//GEN-LAST:event_dltbtnActionPerformed

