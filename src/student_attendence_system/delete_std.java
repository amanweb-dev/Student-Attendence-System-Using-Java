    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        try {
            // TODO add your handling code here:
            String id = srchField.getText();
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/student_attendance","root","");
            pst = con.prepareStatement("select * from student WHERE s_id = ?");
            pst.setString(1, id);
            rs = pst.executeQuery();
            srchtbl.setModel(DbUtils.resultSetToTableModel(rs));
            srchtbl.setRowSelectionInterval(0, 0);
     
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(delete_std.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(delete_std.class.getName()).log(Level.SEVERE, null, ex);
        }
            
    }//GEN-LAST:event_searchBtnActionPerformed

